#include <stdlib.h>
#include <stdio.h>

int main()
{
    long long int n;
    int pcs = 0;
    scanf("%lld",&n);
    while(n>=5)
    {
        n /= 5;   //从最后一个数开始，计算可分解出5的个数
        pcs += n; //累计sum
    }
    printf("%d",pcs);

    return 0;
}

//n（1<=n<=2^(31)）。
