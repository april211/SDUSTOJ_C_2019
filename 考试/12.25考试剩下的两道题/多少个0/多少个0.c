//
//要出现0，就需要有2和5，当然10也可以，不过10肯定也能因式分解为2和5，所以也就可以看做2和5。
//我们的最终目的就是寻找这个1-N这些数中能找到多少对2和5，有多少对2和5就有多少个0。
//在0-N的数中，我们假设对每一个数进行分解，都尝试去分解成2和5。
//当然不是所有的数都可以分解成2和5，但是最终分解出来的结果2的数量肯定是多于5的数量的，这一点其实很容易想通，二的倍数实在是太多了。
